package ex_04_Operators;

public class Lab38_Operators_Arithmetic_Operators {
    public static void main(String[] args) {

        int a = 20; //assignment operator
        int b = 3;
        float c = 3.0f;

        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);

        System.out.println(a/c);
        System.out.println("a+b");
        System.out.println(a+b);
    }
}
