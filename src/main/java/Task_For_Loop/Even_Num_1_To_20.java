package Task_For_Loop;

public class Even_Num_1_To_20 {
    public static void main(String[] args) {

        System.out.println("Even Number from 1 To 20: ");

        for (int i = 1; i <= 20; i++) {

            if (i % 2 == 0) {
                System.out.println(i);
            }
        }

    }
}
